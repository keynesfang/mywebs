function item_comment_attach(id, arr) {
    var obj = top.Dbg.DebugFrame.document;
    var cfg = obj.getElementById(id);
    var table = cfg.children[0];
    var tbody = table.children[0];
    var trs = tbody.children;
    for (var i = 0; i < trs.length; i++) {
        var node_text = trs[i].children[0].innerHTML;
        if (node_text.length > 2) {
            if (typeof(arr[node_text]) != "undefined" && arr[node_text].length > 0) {
                trs[i].children[0].innerHTML = node_text + " - " + arr[node_text];
            }
        }
    }
}

